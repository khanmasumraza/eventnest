// Request refund
const requestRefund = async (req, res) => {
  try {
    const { registrationId } = req.params
    const userId = req.user.id

    const ticket = await Ticket.findById(registrationId)
    if (!ticket) {
      return res.status(404).json({ message: 'Registration not found' })
    }

    if (ticket.user.toString() !== userId) {
      return res.status(403).json({ message: 'Not authorized' })
    }

    if (ticket.status !== 'paid') {
      return res
        .status(400)
        .json({ message: 'Only paid tickets can be refunded' })
    }

    const payment = await Payment.findOne({ ticket: ticket._id })
    if (payment) {
      payment.status = 'refund_requested'
      await payment.save()
    }

    ticket.status = 'refund_requested'
    await ticket.save()

    createAuditLog('REFUND_REQUESTED', userId, ticket.event, {
      ticketId: ticket.ticketId,
      paymentId: payment?._id,
    })

    res.status(200).json({
      success: true,
      message: 'Refund requested successfully',
    })
  } catch (error) {
    console.error('Request refund error:', error)
    res.status(500).json({ message: 'Error requesting refund' })
  }
}

