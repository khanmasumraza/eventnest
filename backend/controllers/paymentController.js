// Helper used for test UPI flow: create a verified registration with QR code
const generateTicket = async ({ payment, event, userId }) => {
  // Create registration; ticketId & digital signature will be generated by pre-save hook
  let registration = await Ticket.create({
    event: event._id,
    user: userId,
    paymentId: payment._id,
    amount: payment.amount,
    paymentStatus: 'verified',
    paymentMethod: 'upi',
    upiId: event.paymentDetails?.upiId || event.organizerUpiId || '',
    utrNumber: payment.referenceId,
    status: 'paid',
    attendeeInfo: {},
  })

  // Ensure we have the generated ticketId before creating QR
  registration = await Ticket.findById(registration._id)

  const qrPayload = {
    ticketId: registration.ticketId,
    eventId: String(event._id),
    userId: String(userId),
  }

  const qrCode = await QRCode.toDataURL(JSON.stringify(qrPayload), {
    width: 300,
    margin: 2,
  })

  registration.qrCode = qrCode
  await registration.save()

  // Increment event registered count
  await Event.findByIdAndUpdate(event._id, { $inc: { registered: 1 } })

  return registration
}

