// Handle webhook
const handleWebhook = async (req, res) => {
  const webhookVerified = req.webhookVerified
  const eventId = req.eventId

  if (!webhookVerified) {
    return sendStandardResponse(res, false, 'Webhook not verified')
  }

  try {
    const razorpayEvent = req.body.event
    const razorpayEntity = req.body.entity

    paymentLogger.logWebhook(eventId, true, `Processing ${razorpayEvent}`)

    if (razorpayEvent !== 'payment.captured') {
      return sendStandardResponse(res, true, `Event ${razorpayEvent} ignored`, {
        eventId,
      })
    }

    // Find payment by Razorpay payment ID
    const razorpayPaymentId = razorpayEntity.id
    const payment = await Payment.findOne({ razorpayPaymentId })

    if (!payment) {
      return sendStandardResponse(res, false, 'Payment not found')
    }

    if (payment.webhookProcessedAt) {
      await createAuditLog(
        'WEBHOOK_DUPLICATE',
        null,
        payment.event,
        {
          webhookEventId: eventId,
          paymentId: payment._id,
          alreadyProcessedAt: payment.webhookProcessedAt,
        },
        payment._id,
      )
      return sendStandardResponse(res, true, 'Already processed')
    }

    // Atomic update with webhook idempotency
    const session = await mongoose.startSession()
    await session.withTransaction(async () => {
      // Double-check status
      const updatedPayment = await Payment.findById(payment._id).session(
        session,
      )
      if (updatedPayment.status !== 'pending') {
        throw new Error('Payment not pending')
      }

      // Mark webhook processed + update status
      updatedPayment.status = 'verified'
      updatedPayment.webhookEventId = eventId
      updatedPayment.webhookProcessedAt = new Date()
      updatedPayment.processedAt = new Date()
      updatedPayment.completedAt = new Date()
      await updatedPayment.save({ session })

      // Check capacity atomically
      const registeredCount = await Ticket.countDocuments({
        event: payment.event,
        paymentStatus: 'verified',
      }).session(session)

      const eventDoc = await Event.findById(payment.event).session(session)
      if (registeredCount >= eventDoc.capacity) {
        throw new Error('Event full')
      }

      // Create ticket (auto-sync via pre-save hook)
      const registration = new Ticket({
        event: payment.event,
        user: payment.user,
        paymentId: payment._id,
        amount: payment.amount,
        paymentStatus: 'verified',
        paymentMethod: 'razorpay',
        razorpayPaymentId: razorpayPaymentId,
        razorpayOrderId: payment.razorpayOrderId,
        attendeeInfo: {},
      })
      await registration.save({ session })

      // Generate QR
      const qrPayload = {
        ticketId: registration.ticketId,
        eventId: String(payment.event),
        userId: String(payment.user),
      }
      const qrCode = await generateTicketQR(qrPayload)
      registration.qrCode = qrCode
      await registration.save({ session })

      // Atomic event counter update
      await Event.findByIdAndUpdate(
        payment.event,
        { $inc: { registeredCount: 1, registered: 1 } },
        { session },
      )

      // Audit & sockets
      await createAuditLog(
        'TICKET_WEBHOOK_CREATED',
        payment.user,
        payment.event,
        {
          webhookEventId: eventId,
          paymentId: payment._id,
          ticketId: registration.ticketId,
          eventType: 'payment.captured',
        },
        payment._id,
      )

      emitToEvent(String(payment.event), 'ticketCreated', {
        ticketId: registration.ticketId,
        webhookEventId: eventId,
      })
      emitToOrganizer(eventDoc.organiser.toString(), 'paymentCaptured', {
        paymentId: payment._id,
        ticketId: registration.ticketId,
        amount: payment.amount,
      })
    })

    await createAuditLog('WEBHOOK_SUCCESS', null, null, {
      webhookEventId: eventId,
      razorpayPaymentId,
      status: 'completed',
    })

    sendStandardResponse(res, true, 'Payment captured & ticket created')
  } catch (error) {
    paymentLogger.logWebhook(eventId, false, 'processing_failed', error)
    await createAuditLog('WEBHOOK_FAILED', null, null, {
      webhookEventId: eventId,
      error: error.message,
    })
    sendStandardResponse(
      res,
      false,
      'Webhook processing failed',
      {},
      error.message,
    )
  }
}

