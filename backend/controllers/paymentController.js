// Helper: Standard response
const sendStandardResponse = (
  res,
  success,
  message,
  data = {},
  error = null,
) => {
  res.status(success ? 200 : 400).json({
    success,
    message,
    data,
    error,
  })
}

// Helper: Create audit log
const createAuditLog = async (
  action,
  userId,
  eventId,
  data = {},
  paymentId = null,
) => {
  try {
    await PaymentAudit.create({
      paymentId,
      eventId,
      userId,
      webhookEventId: data.webhookEventId || null,
      webhookEventType: data.eventType || 'manual',
      action,
      status: data.status || 'info',
      ipAddress: data.ip || 'unknown',
      razorpayPayload: data.payload
        ? JSON.stringify(data.payload).slice(0, 1000)
        : null,
    })
    paymentLogger.logPaymentEvent(action, { userId, eventId, paymentId, data })
  } catch (err) {
    paymentLogger.logPaymentEvent(action, { userId, eventId, paymentId }, err)
  }
}

