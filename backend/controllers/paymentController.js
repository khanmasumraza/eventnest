// PROCESS PAYMENT - Creates ticket with pending status, waits for organizer verification
const processPayment = async (req, res) => {
  try {
    const { eventId, attendeeInfo, referenceId, screenshot } = req.body
    const userId = req.user.id

    console.log('PROCESS PAYMENT BODY:', req.body)

    // Validate required fields
    if (!eventId) {
      return res.status(400).json({ message: 'Event ID is required' })
    }

    // Get event
    const event = await Event.findById(eventId)
    if (!event) {
      return res.status(404).json({ message: 'Event not found' })
    }

    const isFree = !event.price || event.price === 0

    // Check if user already has a ticket for this event
    let ticket = await Ticket.findOne({
      user: userId,
      event: eventId,
    })

    // If ticket exists and is already paid, return error
    if (
      ticket &&
      (ticket.status === 'paid' || ticket.status === 'checked_in')
    ) {
      return res.status(400).json({
        message: 'You already have a ticket for this event',
        ticketId: ticket.ticketId,
      })
    }

    // Create ticket with PENDING status (not paid yet!)
    if (!ticket) {
      const ticketId = generateTicketId()
      ticket = new Ticket({
        ticketId,
        user: userId,
        event: eventId,
        status: 'pending', // IMPORTANT: Starts as pending
        attendeeInfo: attendeeInfo || {},
      })
      await ticket.save()
      console.log('Created pending ticket:', ticketId)
    }

    // For free events, directly verify
    if (isFree) {
      // Generate QR code
      const qrCode = await QRCode.toDataURL(ticket.ticketId, {
        width: 300,
        margin: 2,
      })

      // Update ticket to paid
      ticket.status = 'paid'
      ticket.qrCode = qrCode
      await ticket.save()

      // Update event registered count
      await Event.findByIdAndUpdate(eventId, {
        $inc: { registered: 1 },
      })

      // Emit event
      emitToEvent(eventId, 'ticketActivated', { ticketId: ticket.ticketId })
      emitToOrganizer(event.organiser.toString(), 'ticketPurchased', {
        ticketId: ticket.ticketId,
        eventId,
        userId,
      })

      createAuditLog('FREE_TICKET_CREATED', userId, eventId, {
        ticketId: ticket.ticketId,
      })

      return res.status(201).json({
        success: true,
        ticket,
        message: 'Registration successful!',
      })
    }

    // For PAID events: Create payment with PENDING status
    const paymentAmount = event.price

    // Check for existing pending payment
    const existingPayment = await Payment.findOne({
      ticket: ticket._id,
      status: 'pending',
    })

    if (existingPayment) {
      return res.status(400).json({
        message: 'Payment already submitted, waiting for verification',
        paymentId: existingPayment._id,
        ticketId: ticket.ticketId,
        status: 'pending',
      })
    }

    // Validate referenceId for paid events
    if (!referenceId) {
      return res
        .status(400)
        .json({ message: 'Payment reference ID is required for paid events' })
    }

    // Create payment with PENDING status
    const payment = new Payment({
      event: eventId,
      user: userId,
      ticket: ticket._id,
      amount: paymentAmount,
      method: 'upi',
      status: 'pending', // IMPORTANT: Starts as pending
      referenceId: referenceId,
      screenshot: screenshot || '',
    })

    await payment.save()
    console.log('Created pending payment:', payment._id)

    // Emit to organizer about new payment
    emitToOrganizer(event.organiser.toString(), 'paymentSubmitted', {
      ticketId: ticket.ticketId,
      paymentId: payment._id,
      eventId,
      amount: paymentAmount,
      userId,
    })

    createAuditLog('PAYMENT_SUBMITTED', userId, eventId, {
      ticketId: ticket.ticketId,
      paymentId: payment._id,
      amount: paymentAmount,
    })

    // Return ticket and payment info
    res.status(201).json({
      success: true,
      ticket,
      payment,
      message:
        'Payment submitted successfully! Waiting for organizer verification.',
    })
  } catch (error) {
    console.error('Process payment error:', error)
    res
      .status(500)
      .json({ message: 'Error processing payment: ' + error.message })
  }
}

// Submit payment for ticket (after ticket is created)
const submitPayment = async (req, res) => {
  try {
    const { ticketId } = req.params
    const { amount, method, referenceId, screenshot } = req.body
    const userId = req.user.id

    // Validate required fields
    if (!referenceId) {
      return res
        .status(400)
        .json({ message: 'Payment reference ID is required' })
    }

    const ticket = await Ticket.findOne({ ticketId }).populate('event')
    if (!ticket) {
      return res.status(404).json({ message: 'Ticket not found' })
    }

    // Security: Ensure ticket belongs to user
    if (ticket.user.toString() !== userId) {
      return res
        .status(403)
        .json({ message: 'Not authorized to submit payment for this ticket' })
    }

    if (ticket.status !== 'pending') {
      return res
        .status(400)
        .json({ message: 'Ticket is not in pending status' })
    }

    // Check for existing pending payment
    const existingPayment = await Payment.findOne({
      ticket: ticket._id,
      status: 'pending',
    })

    if (existingPayment) {
      return res.status(400).json({
        message: 'Payment already submitted, waiting for verification',
        paymentId: existingPayment._id,
      })
    }

    // Create payment with PENDING status
    const payment = new Payment({
      event: ticket.event._id,
      user: ticket.user,
      ticket: ticket._id,
      amount: amount || ticket.event.price,
      method: method || 'upi',
      status: 'pending',
      referenceId,
      screenshot: screenshot || '',
    })

    await payment.save()

    // Emit to organizer
    emitToOrganizer(ticket.event.organiser.toString(), 'paymentSubmitted', {
      ticketId,
      paymentId: payment._id,
      eventId: ticket.event._id,
      amount: payment.amount,
      userId: ticket.user,
    })

    createAuditLog('PAYMENT_SUBMITTED', userId, ticket.event._id, {
      paymentId: payment._id,
      ticketId,
      amount: payment.amount,
    })

    res.status(201).json({
      success: true,
      payment,
      message: 'Payment submitted for verification',
    })
  } catch (error) {
    console.error('Submit payment error:', error)
    res.status(500).json({ message: 'Error submitting payment' })
  }
}

// Get payment status
const getPaymentStatus = async (req, res) => {
  try {
    const { orderId, ticketId } = req.params // Support both orderId (polling) and ticketId
    const userId = req.user.id

    let query = {}
    if (orderId === 'null') {
      // Legacy ticketId route
      query = { ticket: ticketId }
    } else {
      query = { razorpayOrderId: orderId }
    }

    const payment = await Payment.findOne(query)
      .populate('event', 'title date organizerUpiId')
      .populate('ticket', 'ticketId status qrCode')
      .populate('verifiedBy', 'name email')
      .sort({ createdAt: -1 })

    if (!payment) {
      return sendStandardResponse(res, false, 'Payment not found')
    }

    // Authorization
    if (payment.user.toString() !== userId) {
      const isOrganizer =
        payment.event && payment.event.organiser.toString() === userId
      if (!isOrganizer) {
        return sendStandardResponse(res, false, 'Not authorized')
      }
    }

    // Include ticket status for polling
    const ticketStatus = payment.ticket ? payment.ticket.status : null
    const isProcessing =
      !payment.webhookProcessedAt && payment.status === 'pending'

    sendStandardResponse(res, true, 'Status fetched', {
      payment,
      ticketStatus,
      isWebhookProcessing: isProcessing,
      pollingRecommended: isProcessing,
      organizerUpiId: payment.event?.organizerUpiId,
    })
  } catch (error) {
    paymentLogger.logPaymentEvent(
      'GET_STATUS_ERROR',
      req.user?.id,
      null,
      null,
      error,
    )
    sendStandardResponse(res, false, 'Error fetching status', {}, error.message)
  }
}

// Manual verification (legacy for non-Razorpay)
const manualVerifyPayment = async (req, res) => {
  try {
    const { paymentId } = req.params
    const { status, rejectionReason } = req.body
    const organizerId = req.user.id

    console.log('MANUAL VERIFY PAYMENT:', paymentId, status)

    // Validate status
    if (!status || !['verified', 'rejected'].includes(status)) {
      return res
        .status(400)
        .json({ message: 'Invalid status. Use "verified" or "rejected"' })
    }

    const payment =
      await Payment.findById(paymentId).populate('event ticket user')
    if (!payment) {
      return res.status(404).json({ message: 'Payment not found' })
    }

    // SECURITY: Verify organizer owns the event
    if (payment.event.organiser.toString() !== organizerId) {
      return res
        .status(403)
        .json({ message: 'Not authorized to verify payments for this event' })
    }

    if (payment.status !== 'pending') {
      return res.status(400).json({ message: 'Payment already processed' })
    }

    // Update payment status
    payment.status = status
    payment.verifiedBy = organizerId
    payment.verifiedAt = new Date()

    if (status === 'verified') {
      payment.markVerified(organizerId)

      // Generate QR code for ticket (ONLY NOW!)
      const qrCode = await QRCode.toDataURL(payment.ticket.ticketId, {
        width: 300,
        margin: 2,
      })

      // Update ticket to PAID and add QR code
      payment.ticket.status = 'paid'
      payment.ticket.qrCode = qrCode
      payment.ticket.paymentVerified = true
      await payment.ticket.save()

      // Update event tickets sold
      await Event.findByIdAndUpdate(payment.event._id, {
        $inc: { registered: 1 },
      })

      // Emit real-time updates
      emitToEvent(payment.event._id.toString(), 'ticketActivated', {
        ticketId: payment.ticket.ticketId,
      })

      emitToOrganizer(organizerId, 'paymentApproved', {
        ticketId: payment.ticket.ticketId,
        paymentId: payment._id,
        amount: payment.amount,
        userName: payment.user.name,
      })

      createAuditLog(
        'MANUAL_PAYMENT_VERIFIED',
        organizerId,
        payment.event._id,
        {
          paymentId: payment._id,
          ticketId: payment.ticket.ticketId,
          amount: payment.amount,
        },
      )
    } else {
      // Rejected
      payment.ticket.status = 'rejected'
      await payment.ticket.save()

      createAuditLog('PAYMENT_REJECTED', organizerId, payment.event._id, {
        paymentId: payment._id,
        reason: rejectionReason || 'Not specified',
      })
    }

    await payment.save()

    res.status(200).json({
      success: true,
      payment,
      ticket: payment.ticket,
      message:
        status === 'verified'
          ? 'Payment verified! Ticket activated.'
          : 'Payment rejected',
    })
  } catch (error) {
    console.error('Manual verify payment error:', error)
    res.status(500).json({ message: 'Error verifying payment' })
  }
}

